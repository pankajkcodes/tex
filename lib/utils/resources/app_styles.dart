import 'package:flutter/material.dart';

class AppStyle {
  static TextStyle kTextStyle14(Color color) {
    return TextStyle(fontSize: 14, color: color);
  }

  static TextStyle kTextStyle16(Color color) {
    return TextStyle(fontSize: 16, color: color);
  }

  static TextStyle kTextStyle18(Color color) {
    return TextStyle(fontSize: 18, color: color);
  }

  static TextStyle kTextStyle18B(Color color) {
    return TextStyle(fontSize: 18, color: color, fontWeight: FontWeight.bold);
  }

  static TextStyle kTextStyle20(Color color) {
    return TextStyle(fontSize: 20, color: color);
  }
  static TextStyle kTextStyle20B(Color color) {
    return TextStyle(fontSize: 20, color: color,fontWeight: FontWeight.bold);
  }

  static TextStyle kTextStyle24Bold(Color color) {
    return TextStyle(fontSize: 34, color: color, fontWeight: FontWeight.bold);
  }
}
